public class Switch{
    public static void main (String args[]){
        int num = 3;
        switch (num){
            case 1 : System.out.println(" get out");
            break;
            case 2 : System.out.println(" from here");
            break;
            case 3 : System.out.println("i saw you");
            break;
            default : System.out.println("this is default");
        }
    }
} 